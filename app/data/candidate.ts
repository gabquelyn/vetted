export function getCityCluster(city: string): number | null {
  if (!city) return null
  const key = city.toLowerCase().replace(/,.*$/, '').trim()
  if (CITY_CLUSTER[key] !== undefined) return CITY_CLUSTER[key]
  for (const k in CITY_CLUSTER) {
    if (key.indexOf(k) !== -1 || k.indexOf(key) !== -1) return CITY_CLUSTER[k]
  }
  return 6
}

